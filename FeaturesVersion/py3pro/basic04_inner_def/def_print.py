# print()函数的使用示例
# 输出字符串
print('Hello, SteveRocket1!')  # Hello, SteveRocket1!

# 输出多个参数
print('Hello,', 'SteveRocket2!')  # Hello, SteveRocket2!

# 输出多个参数并指定分隔符
print('Hello,', 'SteveRocket3!', sep=' ')  # Hello, SteveRocket3!
print('Hello,', 'SteveRocket3!', sep=' - ') # Hello, - SteveRocket3!

# 输出多个参数并指定末尾字符
print('Hello,', end=' * ')  # Hello, SteveRocket4!
print('SteveRocket4!')


print("Hello", ),
print("SteveRocket5!")

for i in range(5):
    print(i,)

for i in range(5):
    print(i, end=" ")





















